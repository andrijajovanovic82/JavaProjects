package D_21_Andrija_Jovanovic;

public class Radnik {

    //Napraviti klasu Radnik koja ima atribute id, naziv radnika, godine iskustva i plata.

    String id;
    String nazivRadnika;
    int godineIskustva;
    int plata;

    public Radnik(String id, String nazivRadnika, int godineIskustva, int plata) {
        this.id = id;
        this.nazivRadnika = nazivRadnika;
        this.godineIskustva = godineIskustva;
        this.plata = plata;
    }
}
